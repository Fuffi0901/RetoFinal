package modelo;

public class Canta {
	//Atributos.
	private int codCancion;
	private String Dni;
	
	//Getters y Setters.
	public int getCodCancion() {
		return codCancion;
	}
	public void setCodCancion(int codCancion) {
		this.codCancion = codCancion;
	}
	public String getDni() {
		return Dni;
	}
	public void setDni(String dni) {
		Dni = dni;
	}
}
